import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sourcesDir = path.join(rootDir, "shared", "data", "grantSources");
const manifestPath = path.join(sourcesDir, "manifest.json");
const outputPath = path.join(rootDir, "shared", "generatedGrantSourceLibrary.ts");

const sourceFiles = readdirSync(sourcesDir)
  .filter((file) => file.endsWith(".json") && file !== "manifest.json")
  .sort((a, b) => a.localeCompare(b));

let manifest = [];
try {
  manifest = JSON.parse(readFileSync(manifestPath, "utf8"));
} catch {
  manifest = [];
}

const knownFiles = new Set(sourceFiles);
const orderedFiles = [
  ...manifest.filter((file) => knownFiles.has(file)),
  ...sourceFiles.filter((file) => !manifest.includes(file)),
];

const libraryKeys = new Set();
for (const file of orderedFiles) {
  const source = JSON.parse(readFileSync(path.join(sourcesDir, file), "utf8"));
  if (!source.libraryKey || typeof source.libraryKey !== "string") {
    throw new Error(`${file} is missing a string libraryKey`);
  }
  if (libraryKeys.has(source.libraryKey)) {
    throw new Error(`${file} duplicates libraryKey ${source.libraryKey}`);
  }
  libraryKeys.add(source.libraryKey);
}

const toIdentifier = (file) =>
  file
    .replace(/\.json$/, "")
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((part, index) => {
      const normalized = part.replace(/^[0-9]+/, "");
      if (!normalized) return "";
      const lower = normalized[0].toLowerCase() + normalized.slice(1);
      return index === 0 ? lower : lower[0].toUpperCase() + lower.slice(1);
    })
    .join("");

const imports = orderedFiles
  .map((file) => `import ${toIdentifier(file)} from "./data/grantSources/${file}";`)
  .join("\n");

const exportsList = orderedFiles.map((file) => `  ${toIdentifier(file)},`).join("\n");

const output = `${imports}

// Generated by scripts/generate-grant-source-library.mjs. Edit the JSON files in
// shared/data/grantSources instead of editing this registry by hand.
export const GENERATED_BUILT_IN_GRANT_SOURCES = [
${exportsList}
];
`;

writeFileSync(manifestPath, `${JSON.stringify(orderedFiles, null, 2)}\n`);
writeFileSync(outputPath, output);
